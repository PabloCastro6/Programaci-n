
public class Jubilacion {

	public static void main(String[] args) {
		//declaramos la variabale edad
		int edad;
		//declaramos la variable edad de jubilación
		int edadJubilacion;
		//declaramos la variable que almacenará los años restantes hasta la jubilación
		int anhosJubilacion;
		
		//inicializamos variable
		edad = 30;
		edadJubilacion = 67;
		
		//Calculamos la resta y el resultado lo almacenamos en la variable anhosJubilacion
		 anhosJubilacion = edadJubilacion - edad;
		
		System.out.println("Los años que te faltan para jubilarte son: " + anhosJubilacion); 
	
	}
}
